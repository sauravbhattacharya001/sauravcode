#!/usr/bin/env python3
"""sauravtranspile — Transpile sauravcode (.srv) to readable Python.

Converts .srv source files into clean, idiomatic Python 3 code.
This enables sauravcode prototypes to be deployed as Python scripts,
integrated into Python projects, or used as a learning bridge between
the two languages.

The transpiler reuses the sauravcode parser to build an AST, then
walks it to emit Python source.  Most language features map naturally:

  - Functions → ``def`` with Python indentation
  - For loops → ``for i in range(start, end)``
  - For-each → ``for item in collection``
  - Match/case → Python 3.10+ ``match``/``case``
  - Pipe ``|>`` → nested function calls
  - Enums → ``enum.IntEnum`` subclasses
  - Try/catch → ``try``/``except``
  - F-strings, lambdas, list comprehensions → direct equivalents

Builtins that have direct Python equivalents (len, print, range, etc.)
are emitted as-is.  Sauravcode-specific builtins (contains, starts_with,
etc.) are emitted as method calls or helper function calls with a small
runtime preamble.

Usage::

    python sauravtranspile.py program.srv                  # print to stdout
    python sauravtranspile.py program.srv -o program.py    # write to file
    python sauravtranspile.py program.srv --no-preamble    # skip runtime helpers
    python sauravtranspile.py program.srv --verify         # transpile + run both, compare output

CLI::

    python sauravtranspile.py input.srv [-o OUTPUT] [--no-preamble] [--verify]
"""

